    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if("".equals(idCliente.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el identificador del cliente");
        if("".equals(pres.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el monto del prestamo");
        if("".equals(fcha.getText())){ JOptionPane.showMessageDialog(null, "Todo registro debe tener una fecha");}}}
        else{  elegir_id = idp.getText();    idPres = idCliente.getText();        
        monto= pres.getText();               fecha = fcha.getText();
        guardar.agregar_Prestamo(elegir_id,idPres, monto, fecha);
        idCliente.setText("");        pres.setText("");
        fcha.setText("");             idp.setText("");
        mostrar.mostrar_prestamos();}
    }//GEN-LAST:event_jButton3ActionPerformed

    private void MitablaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MitablaMousePressed
        JTable table = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int row = table.rowAtPoint(point);
        if(evt.getClickCount() == 2){
            idp.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(), 0));
            idCliente.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(), 1));
            pres.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(), 2));
            fcha.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(),3));
        }
    }//GEN-LAST:event_MitablaMousePressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if("".equals(idp.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el identificador del presatamo a borrar");
        if("".equals(idCliente.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el identificador del cliente");
        if("".equals(pres.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el monto del prestamo a borrar");}}}
        else{ elegir_id = idp.getText();        idPres = idCliente.getText();
        monto = pres.getText();
        
        eliminar.eliminar_prestamo(elegir_id,monto,idPres);
        
        idp.setText("");        idCliente.setText("");
        pres.setText("");
        mostrar.mostrar_prestamos();}
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       if( "".equals(idPago.getText())){
           JOptionPane.showMessageDialog(null, "Ingresa el identificador del cliente");
       if("".equals(pago.getText())){ 
           JOptionPane.showMessageDialog(null, "Ingresa el monto del prestamo"); 
       if( "".equals(fcha.getText())){
           JOptionPane.showMessageDialog(null, "Todo registro debe contener su fecha"); }}}
       else{   
        elegir_id = idp.getText();     id = idPago.getText();
        monto = pago.getText();        fecha = fcha.getText();  
        
        guardar.agregar_Pago(elegir_id,id, monto, fecha);
        
        idPago.setText("");      pago.setText("");
        fcha.setText("");
        mostrar.mostrar_pagos();
       }
        
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if("".equals(idp.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el identificador del pago que deseas borrar");
        if("".equals(pago.getText())){ JOptionPane.showMessageDialog(null, "Ingresa monto del pago que deseas borrar");
        if("".equals(idPago.getText())){ JOptionPane.showMessageDialog(null, "Ingresa el identificador del cliente");
        }}}
        else{  elegir_id = idp.getText();        id = idPago.getText();
               monto = pago.getText();          
               eliminar.eliminar_pago(elegir_id,monto,id);
        idp.setText("");        idPago.setText("");
        pago.setText("");
        mostrar.mostrar_pagos();}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void MitablaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MitablaMousePressed
        JTable table = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int row = table.rowAtPoint(point);
        if(evt.getClickCount() == 2){
            idp.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(), 0));
            idPago.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(), 1));
            pago.setText((String)Mitabla.getValueAt(Mitabla.getSelectedRow(), 2));
        }
    }//GEN-LAST:event_MitablaMousePressed


    private void limpiarTabla() {
        try{
            DefaultTableModel temp = (DefaultTableModel) Mitabla.getModel();
            int a =temp.getRowCount()-1;
            for(int i=0; i < a; i++){
               Mitabla.setValueAt("", i, 0);
               Mitabla.setValueAt("", i, 1);
               Mitabla.setValueAt("", i, 2);
               Mitabla.setValueAt("", i, 3);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }

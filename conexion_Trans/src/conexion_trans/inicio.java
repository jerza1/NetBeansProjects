    private void NwebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NwebActionPerformed
       String web = JOptionPane.showInputDialog(null, "Introduzca direccion web");
       try{
           URI dirWeb = new URI(web);
           Desktop escritorio = null;
           if(Desktop.isDesktopSupported()){
               escritorio = Desktop.getDesktop();
           }
          if(escritorio != null){
              escritorio.browse(dirWeb);
          }
       }catch(IOException io){
           io.printStackTrace();
           JOptionPane.showMessageDialog(null, "Error inesperado", "Error", JOptionPane.ERROR_MESSAGE);
       }catch(URISyntaxException u){
           u.printStackTrace();
           JOptionPane.showMessageDialog(null, "Introduzca una dirección web válida", "Error", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_NwebActionPerformed

